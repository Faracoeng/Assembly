    MOV R0,#00H
    MOV R1, #00H
VERDE: clr TF0
    MOV P2, #11100000b
    CLR TR1
    setb tr0    ;aciona o timer zero
    JNB TF0,$    ;CIFRAÕ AMNTÉM EM LOOP NESSA LINHA WHILE(TF0==0)
    INC R0
    CJNE R0, #0FFH,VERDE
    CLR TF0
    CLR TR0
    MOV R0,#00H
    JMP AMARELO
AMARELO:
    CLR TF1
    MOV P2,#00011000b
    SETB TR1    ;ACIONA O TIMER 1
    JNB TF1,$
    INC R1
    CJNE R1, #0FFH, AMARELO
    CLR TF1
    CLR TR1
    MOV R1, #00H
    JMP VERMELHO
        MOV P2, #00000111b
VERMELHO:
    MOV P2, #00000111b
    CLR TR1
    setb tr0    ;aciona o timer zero
    JNB TF0,$    ;CIFRAÕ AMNTÉM EM LOOP NESSA LINHA WHILE(TF0==0)
    INC R0
    CJNE R0, #0FFH,VERMELHO
    CLR TF0
    CLR TR0
    MOV R0,#00H
    JMP VERDE
    end

